char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (1)
	{
		dest[i + j] = src[j];
		if (dest[i + j] == '\0')
			break ;
		j++;
	}
	return (dest);
}
